    public void setName(String name) {
        this.name = name;
    }

    public String getShort_name() {
        return short_name;
    }

    public void setShort_name(String short_name) {
        this.short_name = short_name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTuan_price() {
        return tuan_price;
    }

    public void setTuan_price(String tuan_price) {
        this.tuan_price = tuan_price;
    }

    public int getTuan_discount() {
        return tuan_discount;
    }

    public void setTuan_discount(int tuan_discount) {
        this.tuan_discount = tuan_discount;
    }
